
public class OperadoresRelacionais {
	
	public static void main (String[] args) {
		//false, false,true, true, false e true nessa ordem
		
		int a = 1;
		int b = 2;
		
	System.out.println(a==b);
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a<=b);
	System.out.println(a>=b);
	System.out.println(a!=b);
	}
}


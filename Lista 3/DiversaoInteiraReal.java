public class DiversaoInteiraReal {
	
	public static void main (String[] args) {
		
		 // 2  2.5  2.5  2.5  e  2.0
		 int a = 5;
		 int b = 2;
		
		 System.out.println(  a / b );
		 System.out.println( (double) a / (double) b);
		 System.out.println( a / (double) b);
		 System.out.println( (double) a / b );
		 System.out.println(  (double) (a / b));
		 
		
	}
}

